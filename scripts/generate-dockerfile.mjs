#!/usr/bin/env node
// Generates Dockerfile from plugins.json.
// Usage: PLUGINS=care_excalidraw,care_analytics npm run docker

import { readFileSync, writeFileSync } from "fs";
import { resolve } from "path";

const ROOT = resolve(import.meta.dirname, "..");
const registry = JSON.parse(readFileSync(resolve(ROOT, "plugins.json"), "utf8"));

const requested = process.env.PLUGINS
    ? process.env.PLUGINS.split(",").map((s) => s.trim()).filter(Boolean)
    : null;

const plugins = requested
    ? registry.plugins.filter((p) => requested.includes(p.slug))
    : registry.plugins;

if (plugins.length === 0) {
    console.error(
        requested
            ? `No plugins matched: ${requested.join(", ")}. Check slugs in plugins.json.`
            : "plugins.json has no plugins defined."
    );
    process.exit(1);
}

const lines = [];

lines.push("# syntax=docker/dockerfile:1");
lines.push("# Generated by scripts/generate-dockerfile.mjs. Do not edit directly.");
lines.push("ARG REGISTRY_BASE_URL=https://plugins.ohc.network");
lines.push("");
lines.push("# Base: node + git");
lines.push("");
lines.push("FROM node:22-alpine AS base");
lines.push("RUN apk add --no-cache git");
lines.push("");
lines.push("# Plugin builds (run in parallel by BuildKit)");
lines.push("");

for (const plugin of plugins) {
    lines.push(`FROM base AS build-${plugin.slug}`);
    lines.push("WORKDIR /plugin");
    lines.push(`RUN git clone --depth=1 --branch=${plugin.ref} \\`);
    lines.push(`    https://github.com/${plugin.repo}.git .`);
    lines.push("RUN --mount=type=cache,target=/root/.npm \\");
    lines.push("    npm ci --cache /root/.npm");
    lines.push("RUN npm run build");
    lines.push("");
}

lines.push("# Assemble dist and generate manifest");
lines.push("");
lines.push("FROM base AS assemble");
lines.push("ARG REGISTRY_BASE_URL");
lines.push("WORKDIR /app");
lines.push("");
lines.push("COPY plugins.json .");
lines.push("COPY scripts/generate-manifest.mjs scripts/");
lines.push("");

for (const plugin of plugins) {
    const pad = " ".repeat(Math.max(0, 40 - plugin.slug.length));
    lines.push(`COPY --from=build-${plugin.slug}${pad}/plugin/dist/ merged-dist/${plugin.slug}/`);
}

lines.push("");
lines.push("RUN REGISTRY_BASE_URL=${REGISTRY_BASE_URL} node scripts/generate-manifest.mjs");
lines.push("");
lines.push("# Final image");
lines.push("");
lines.push("FROM caddy:2-alpine AS runtime");
lines.push("COPY Caddyfile /etc/caddy/Caddyfile");
lines.push("COPY --from=assemble /app/merged-dist/ /srv/");
lines.push("EXPOSE 80");

const dockerfile = lines.join("\n") + "\n";
writeFileSync(resolve(ROOT, "Dockerfile"), dockerfile);

const slugs = plugins.map((p) => p.slug).join(", ");
console.log(`Dockerfile generated: ${plugins.length} plugin(s) - ${slugs}`);
